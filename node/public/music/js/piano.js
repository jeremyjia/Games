function getNoteByInterval(oldNote, interval) {
	if (oldNote == "") {
		return "";
	}
	// 把时值相关的符号去掉
	var pattern = /(\^*)(\_*)(\=*)[A-Za-z](\,*)(\'*)/g;
	var obj = pattern.exec(oldNote);
	var len = "";
	if (obj != null) {
		len = oldNote.substr(obj[0].length);
	}
	var index = findIndexByNote(obj[0]);
	var newNote = findNoteByIndex(index + interval);
	return newNote + len;
}
