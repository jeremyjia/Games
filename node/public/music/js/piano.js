// 根据调号和音符取得索引号（在StaffValue中）
function findIndexByKAndStaff(K, note) {
	var arr = sd.Simple2Staff.StaffValue;
	var indexArr = sd.Simple2Staff.index;
	var index = -1;
	var referNote = "";
	// 1.先找到C4区域的索引号，然后再根据实际情况取实际索引值
	for (var i = 0; i < arr.length; i++) {
		var obj = arr[i];
		if (obj.K == K) {
			var staffs = obj.STAFF;
			for (var j = 0; j < staffs.length; j++) {
				var n = staffs[j];
				if (n.toLowerCase().replaceAll(",", "").replaceAll("'", "") == note.toLowerCase().replaceAll(",", "").replaceAll("'", "")) {
					index = indexArr[j];
					referNote = n;
				}
			}
		}
	}
	var UPattern = /[A-G]/g;
	var LPattern = /[a-g]/g;
	// 对应标准音符的大小写
	var referNoteUL = "";
	// 传入音符的大小写
	var noteUL = "";
	if (UPattern.test(referNote)) {
		// 大写
		referNoteUL = "U";
	} else if (LPattern.test(referNote)) {
		// 小写
		referNoteUL = "L";
	}

	if (note.match(UPattern) != null) {
		// 大写
		noteUL = "U";
	} else if (note.match(LPattern) != null) {
		// 小写
		noteUL = "L";
	}
	if (noteUL == "U") {
		// 传入音符是大写，则把标准音符也转成大写的索引
		if (referNoteUL == "L") {
			referNote = referNote.toUpperCase();
			index = index - 12;
		}
	}
	if (noteUL == "L") {
		// 传入音符是小写，则把标准音符也转成小写的索引
		if (referNoteUL == "U") {
			referNote = referNote.toLowerCase();
			index = index + 12;
		}
	}
	if (note.indexOf("'") > -1) {
		var n = note.split("'").length - 1
		return index + n * 12;
	} else if (note.indexOf(",") > -1) {
		var n = note.split(",").length - 1;
		return index - n * 12;
	} else {
		return index;
	}

}

