function findIndexByNote(note) {
	note = note.replace(/([cdefgab]),/,function(note,note1){return note1.toUpperCase()});//替换c,这种变成C
	note = note.replace(/([CDEFGAB])'/,function(note,note1){return note1.toLowerCase()});//替换D'这种变成d
	var flag = 0;
	var index = 0;
	// 如果是升号开头，则最后要加1，如果是降号开头，则最后得到的索引号减1，如果是还原符号，则不变
	if (note.substr(0, 1) == "^") {
		flag = 1;
	} else if (note.substr(0, 1) == "_") {
		flag = -1;
	}
	// 去掉升、降、还原符号
	note = replaceYinGao(note);
	for (var i = 0; i < sd.KeyBoard.length; i++) {
		for (var j = 0; j < sd.KeyBoard[i].val.length; j++) {
			if (sd.KeyBoard[i].val[j] == note) {
				index = sd.KeyBoard[i].index[j];
			}
		}
	}
	if (index == 0) {
		for (var i = 0; i < sd.KeyBoard_D.length; i++) {
			for (var j = 0; j < sd.KeyBoard_D[i].val.length; j++) {
				if (sd.KeyBoard_D[i].val[j] == note) {
					index = sd.KeyBoard_D[i].index[j];
				}
			}
		}
	}
	return index + flag;
}
