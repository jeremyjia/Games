function initPbRates (min, max, inc) {
    pbrates = [];
    for (var x = min; x <= max + 0.001; x += inc) { // 0.001 because error in javascript integers !!
        x = Math.round (x * 100) / 100;
        pbrates.push (x);
    }
}

