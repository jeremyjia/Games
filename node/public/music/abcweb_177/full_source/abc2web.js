    $('#vidyub').css ('margin-left',  opt.ctrmed ? (ww - wy).toFixed () + 'px' : '0px');
    $('#aud').css ('margin-left',  opt.ctrmed ? (ww - wa).toFixed () + 'px' : '0px');
